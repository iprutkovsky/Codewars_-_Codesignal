rateLimit = (sb, rm, sa) => {
    let j = 0,
        d = -1,
        r = [];

    let f = t => d - (d = t / 86400 | 0) ? A = [] : 0;

    sb.map((v, i) => {
        let t = v.shift();
        while (rm[j] && rm[j][0] <= t) {
            f(rm[j][0]);
            let k = rm[j++][1];
            A[k] = ~-A[k];
        }
        f(t);
        v.every(u => ~~A[u] < sa) ? v.map(u => A[u] = -~A[u]) : r.push(i);
    });
    return r;
}

